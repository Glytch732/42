#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	if ((s1[i] == '\0' && s2[i] == '\0') || n == 0)
		return (0);
	if (s1[i] == '\0' || s2[i] == '\0')
		return (s1[i] == '\0' ? -1 : 1);
	while (s1[i] == s2[i] && s1[i] != '\0' && i < n)
	{
		i++;
		if (i == n)
			return (0);
	}
	return (((unsigned char)s1[i]) - ((unsigned char)s2[i]));
}
